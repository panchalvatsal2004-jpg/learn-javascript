// Grade Checker

let marks = 72 

if(marks >= 90){
    console.log("grade A+");
}
else if(marks >= 80){
    console.log("garde A");
    
}
else if(marks >= 75){
    console.log("garde B+");
}
else if(marks >= 70){
    console.log("grade B");
}
else if(marks >= 50){
    console.log("pass");
}
else{
    console.log("fail");
    
}